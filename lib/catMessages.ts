// 日替わり（同じ日は同じ・日が変わると変わる）
export function getDailyCatMessage(dateStr: string): string {
  // 日付文字列から安定したインデックスを生成
  let hash = 0;
  for (let i = 0; i < dateStr.length; i++) {
    hash = (hash * 31 + dateStr.charCodeAt(i)) % 1000000;
  }
  return CAT_MESSAGES[hash % CAT_MESSAGES.length];
}
